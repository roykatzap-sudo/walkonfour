/* ════════════════════════════════════════════════════════════
   SEO content layer לעמודי ערים.
   מספק:
   - quickAnswer: 40-60 מילים בראש העמוד
   - faq: שאלות נפוצות לפי עיר
   - SEO title שמכוון ל"גינת כלבים [עיר]" - long-tail value high

   המבנה - אחיד לכל הערים. הנתונים נגזרים מהדאטה הקיימת
   (parks count, walks count) ומשולבים בטמפלייט אנושי.
   ════════════════════════════════════════════════════════════ */

import type { Community } from './communities'

export type CityFaq = { q: string; a: string }

export type CitySeo = {
  /** quickAnswer 40-60 מילים - מופיע מיד אחרי H1. */
  quickAnswer: string
  /** 5-7 שאלות נפוצות. */
  faq: CityFaq[]
  /** ייעוד SEO title - אם רוצים לעקוף את הברירת מחדל. */
  seoTitle?: string
  /** Meta description ספציפי. */
  seoDescription?: string
}

/**
 * בונה SEO content לעיר על בסיס הדאטה הקיימת.
 * עובד אוטומטית לכל 43 ערים - בלי DRY-violations.
 */
export function buildCitySeo(community: Community, parksCount: number, walksCount: number): CitySeo {
  const name = community.name
  const district = community.district
  // מעט מיוחד לערים גדולות
  const isLarge = ['תל אביב-יפו', 'ירושלים', 'חיפה', 'ראשון לציון', 'באר שבע', 'פתח תקווה', 'אשדוד'].includes(name)
  const parkWord = parksCount === 1 ? 'גינת כלבים' : 'גינות כלבים'
  const walkWord = walksCount === 1 ? 'מסלול טיול' : 'מסלולי טיול'

  const quickAnswer = isLarge
    ? `${name} היא מהערים הטובות בארץ לבעלי כלבים, עם ${parksCount} ${parkWord} פעילות${walksCount > 0 ? ` ו-${walksCount} ${walkWord} עם הכלב באזור` : ''}. במדריך הזה תמצאו את הגינות הכי שווה ב${name}, איפה אפשר לשחרר רצועה, ומה החוקים בעיר. הכל מעודכן ומאומת ע"י הקהילה.`
    : `${name} היא עיר במחוז ${district} עם ${parksCount} ${parkWord} פעילות${walksCount > 0 ? ` ו-${walksCount} ${walkWord} באזור` : ''}. בעלי כלבים ב${name} ימצאו כאן את כל המידע - מיקומי הגינות, מסלולי טיול קרובים, וטיפים מהקהילה המקומית.`

  const faq: CityFaq[] = [
    {
      q: `כמה גינות כלבים יש ב${name}?`,
      a: `ב${name} יש ${parksCount} ${parkWord} פעילות שמסומנות במפה שלנו, חלקן מגודרות וחלקן פתוחות. הרשימה מעודכנת מהקהילה - אם אתם מכירים גינה שלא מופיעה, אפשר להוסיף דרך הטופס בסוף העמוד.`,
    },
    {
      q: `איפה מותר לטייל עם כלב ללא רצועה ב${name}?`,
      a: `על פי החוק בישראל, כלב חייב להיות ברצועה בכל מרחב ציבורי. השחרור מותר רק בגינות כלבים מוגדרות ומסומנות. ב${name} - הגינות המגודרות הן המקום הבטוח והחוקי לשחרור.`,
    },
    {
      q: `איזה חוקי כלבים יש ב${name}?`,
      a: `העירייה מחייבת רישיון לכלב (כולל שבב וחיסון כלבת), איסוף צואה, ורצועה בכל מרחב ציבורי. כלבים מסוכנים (לפי פקודת הכלבים) דורשים רישיון נוסף ומחסום. פרטים מלאים ועדכניים באתר העירייה של ${name}.`,
    },
    {
      q: `איפה הווטרינר הקרוב ב${name}?`,
      a: `יש מרפאות וטרינריות פתוחות ב${name} ובאזור. חלק מהמרפאות מציעות שירות 24/7 לחירום. רשימה מומלצת לחירום זמינה בדף הבריאות שלנו - חשוב לשמור את מספר הטלפון מראש, לא לחפש כשהמקרה כבר קורה.`,
    },
    {
      q: `מה לעשות עם הכלב ב${name} בקיץ?`,
      a: `הקיץ הישראלי קשה לכלבים - האספלט מתחמם, יש סכנת מכת חום, ופחות צל. הכללים: לטייל רק בשעות הקריריות (לפני 9:00 ובערב אחרי 19:00), לוודא שתייה זמינה תמיד, ולבדוק את האספלט עם כף היד לפני יציאה לטיול.`,
    },
  ]

  return { quickAnswer, faq }
}

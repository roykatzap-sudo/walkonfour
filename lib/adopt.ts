/** "הכל" + הגדלים - שורת צ'יפים לסינון לפי גודל. */
export const ADOPT_SIZES: AdoptSize[] = ['קטן', 'בינוני', 'גדול']

/** רשימת הערים הייחודיות לאימוץ, ממוינות, עם "כל הערים" בראש (לתפריט הסינון). */
export const adoptCities: string[] = [
  'כל הערים',
  ...Array.from(new Set(adoptDogs.map((d) => d.city))).sort((a, b) =>
    a.localeCompare(b, 'he'),
  ),
]

/** מספר הכלבים הדחופים - לשימוש בכותרת ובמדדים. */
export const urgentCount = adoptDogs.filter((d) => d.urgency === 'דחוף').length

/** ממיין רשימת כלבים לפי דחיפות (דחוף → חדש → רגיל), יציב. */
export function sortByUrgency(dogs: AdoptDog[]): AdoptDog[] {
  return [...dogs].sort((a, b) => URGENCY_ORDER[a.urgency] - URGENCY_ORDER[b.urgency])
}

